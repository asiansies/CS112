import java.util.*;

public class Problem1a {
	
	// Runs insertion sorting on arrays of length, 1, 5, and 15.
	public static void main(String[] args) {
		int[] arr1 = {1};
		int[] arr2 = {2,8,9,4,6};
		int[] arr3 = {4,4,8,3,1,34,5,87,1,17,6,0,3,21,2};

		insertionSort(arr1);
		insertionSort(arr2);
		insertionSort(arr3);
	}

	// Implements insertion sort. Indices are referred to using 
	// zero-based indexing.
	public static void insertionSort(int[] arr) {
		System.out.println("INPUT ARRAY: " + Arrays.toString(arr));
		
		if (arr.length == 1) {
			System.out.println("Nothing to be sorted!");
		}

		// Sets pivot for comparison.
		for (int i = 1; i < arr.length; i++) {
			int temp = arr[i];
			int j;
			
			System.out.println("Pivot is element " + i + "...");
			
			// Compares pivot with previous element in the array.
			for (j = i - 1; j >= 0 && temp < arr[j]; j--) {
				System.out.println("Pivot, " + temp + ", is less than " + arr[j] + "...");
				arr[j + 1] = arr[j];
				System.out.println("Moving " + arr[j] + " to the right of " + temp + ": " + Arrays.toString(arr));
			}

			// Set temp.
			arr[j + 1] = temp;
		}
		
		System.out.println();
		System.out.println("OUTPUT ARRAY: " + Arrays.toString(arr));
		System.out.println("----------------------------------------");
		System.out.println();
		}
}


